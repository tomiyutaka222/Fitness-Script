//オリジナルアイコン
function shortcode_jin_icon_basecode($atts,$content,$tag)  {
	$icon = substr( $tag, strrpos( $tag, '_' ) + 1 );
	extract(shortcode_atts(array(
		'color' => '',
		'size' => '16px',
	),$atts));
	return '<span style="color:'.$color.'; font-size:'.$size.';" class="jic-sc jin-code-icon-'.$icon.'"><i class="jic jin-ifont-'.$icon.'"></i></span>';
}
